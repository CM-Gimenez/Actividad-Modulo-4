package com.academia.actividadm4.Entidades;

/**
 *
 * @author usuario
 */
public class Persona {
    //atributos de la clase person
    private int id;
    private String nombre;
    private String apellido;
    private String email;

    // constructores de clase persona
    public Persona() {
    }

    public Persona(int id, String nombre, String apellido, String email) {
        this.id = id;
        this.nombre = nombre;
        this.apellido = apellido;
        this.email = email;
    }

    //getters y setters de los atributos de la clase persona
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApellido() {
        return apellido;
    }

    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
    
    
    
}
